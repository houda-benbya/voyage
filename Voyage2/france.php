            <h3>L'art</h3>
           <p>Si des productions artistiques sont attestées dans l'espace correspondant à la France actuelle dès la Préhistoire, </p>
           <p>on ne peut parler d'« art français » avant le début du IIe millénaire de notre ère, </p>
           <p>à l'époque où un État et une nation commencent à se former. </p>
           <p>À partir de cette époque, les Beaux-Arts français vont être en grande partie similaires à ceux du reste de l'Europe occidentale, </p>
           <p>voyant se succéder un art roman aux XIe et XIIe siècles et un art gothique du XIIe au XVe siècle </p>
           <p>; la célébration de la puissance de la monarchie française, d'un « espace privilégié »</p>
            <p> et la représentation du sacré font partie des thèmes privilégiés d'un art principalement commandé par le pouvoir politique ou religieux.
            </p>
