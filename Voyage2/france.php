         <h3>L'architecture</h3>
        <p>La France possède un patrimoine architectural riche, </p>
        <p>témoin d'une longue histoire et de la rencontre de différents traits civilisationnels. </p>
        <p>Les 43 180 bâtiments protégés au titre des monuments historiques comprennent principalement des résidences (nombreux châteaux)</p> 
        <p>et des édifices religieux (cathédrales, basiliques, églises),
        <p>mais aussi des monuments et des jardins.</p>
