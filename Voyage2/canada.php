<p class="text-main">Le Canada occupe la majeure partie de la portion septentrionale de l'Amérique du Nord, s'étendant de l'océan Atlantique à l'est jusqu'à l'océan Pacifique à l'ouest, et au nord vers l'océan Arctique. Il partage une frontière terrestre commune avec les États-Unis au sud, et une autre avec ce même pays au nord-ouest (entre l'Alaska et le Yukon d'une part et le nord de la Colombie-Britannique d'autre part). Depuis 2022, il partage aussi une frontière terrestre avec le Danemark (Groenland) grâce au partage entre les deux pays de l'Île Hans. Il partage aussi une frontière maritime avec la France (Saint-Pierre-et-Miquelon). Depuis 1925, le Canada réclame la portion de l'Arctique s'étalant entre les méridiens 60ºO et 141ºO ; cette réclamation n'est toutefois pas universellement reconnue. L'établissement le plus nordique du Canada, et du monde, se situe à Alert (Nunavut), base des Forces armées canadiennes, au sommet de l'île d'Ellesmere (latitude 82,5ºN à 834 kilomètres — 450 milles marins — du Pôle Nord). Le Canada est le plus grand pays du monde après la Russie170.

            La densité de la population, environ 3,5 habitants par kilomètre carré, est l'une des plus faibles au monde. La région la plus densément peuplée du pays est celle du Corridor Québec-Windsor, le long des basses-terres du fleuve Saint-Laurent et des Grands Lacs au sud-est. Au nord de cette région se trouve le vaste bouclier canadien, une région de roc nettoyé par la dernière ère glaciaire, dépourvue de terres fertiles, riche en minéraux, et dotée de lacs et rivières. On y retrouve la forêt boréale, immense forêt nordique composée majoritairement d'épinettes et de sapins. Le Canada possède sur son territoire plus de lacs que tout autre pays dans le monde et possède une importante réserve d'eau douce171,172,173.
            
            Dans l'est du Canada, le fleuve Saint-Laurent se verse dans le golfe du Saint-Laurent, le plus grand estuaire du monde ; l'île de Terre-Neuve se situe en son embouchure, alors que l'Île-du-Prince-Édouard se situe au sud de celui-ci. Le Nouveau-Brunswick et la Nouvelle-Écosse sont séparés par la baie de Fundy, laquelle connaît les marnages les plus grands du monde. Ces quatre provinces maritimes s'avancent à l'est de la péninsule gaspésienne du Québec. L'Ontario et la baie d'Hudson dominent le centre du Canada, alors qu'à l'ouest se trouvent les vastes plaines des Prairies canadiennes, passant par le Manitoba, la Saskatchewan et l'Alberta pour se rendre jusqu'aux montagnes Rocheuses qui les séparent de la Colombie-Britannique.La moyenne des températures absolues hivernales et estivales diffèrent largement d'une région à l'autre. L'hiver peut être très rude dans certaines régions du pays, avec des températures moyennes mensuelles pouvant descendre à −15 °C dans la partie méridionale du pays, bien qu'il soit aussi possible d'atteindre des températures de −45 °C avec de forts vents glaciaux. Les chutes de neige annuelles peuvent atteindre plusieurs centaines de centimètres en moyenne (par exemple, une moyenne de 337 cm à Québec). La côte de la Colombie-Britannique, notamment l'île de Vancouver, constitue une exception, et jouit d'un climat tempéré avec des hivers doux et pluvieux. Pour ce qui est des étés, les températures peuvent grimper jusqu'à 35 °C, voire 40 °C en tenant compte de l'indice humidex. La température la plus froide jamais observée au Canada est de −63 °C à Snag, dans le territoire du Yukon176.

            En 2018, des scientifiques alertent sur la fonte des glaciers du Yukon, celle-ci étant deux fois plus rapide que prévu et pouvant avoir des « conséquences dramatiques » dans la région. Cette accélération a déjà provoqué des changements dans la région : en 2016, en raison du manque d'eau, la rivière Slims qui alimente le lac Kluane a été complètement asséchée. Depuis, le niveau du lac a baissé, provoquant la disparition de milliers de poissons177.
            
            Le réchauffement climatique est deux fois plus rapide au Canada que sur le reste de la planète selon un rapport commandé par le gouvernement canadien. D'après les experts auteurs du rapport, le réchauffement climatique « augmentera la sévérité des vagues de chaleur et contribuera à augmenter les risques de sécheresses et de feux de forêt. Même si les inondations à l’intérieur des terres résultent de multiples facteurs, des précipitations plus intenses augmenteront le risque d’inondation en milieu urbain »178.
            
            En 2019, les chercheurs constatent la fonte du pergélisol des îles arctiques du Canada. Les modèles climatiques établis par le Groupe d'experts intergouvernemental sur l'évolution du climat (GIEC) ne prévoyaient pas un tel dégel avant 2090179.   </p>
      
